function renderTable(col, data) {
    const tableHead = document.querySelector('#dataTable thead tr');
    const tableBody = document.querySelector('#dataTable tbody');

    // create columns
    col.forEach(column => {
        const th = document.createElement('th');
        th.classList.add(column.key);
        th.dataset.hideSwap = column.hideSwap || false;
        th.innerHTML = `
            <div class="text-header text-header-${column.key}">
                ${column.title}
            </div>
        `
        if (column.width) {
            th.style.width = `${column.width}px`;
        }
        tableHead.appendChild(th);
    });

    // create rows
    data?.forEach(record => {
        const row = document.createElement('tr');
        col.forEach(column => {
            const cell = document.createElement('td');
            cell.innerHTML = column.render(record);
            cell.dataset.hideSwap = column.hideSwap || false;
            cell.className = `table-td table-td-${column.key}`
            row.className = 'table-tr'
            row.appendChild(cell);
        });

        // Add click event listener to the row
        row.addEventListener('click', function () {
            if (localStorage.getItem('dataMenuToken') === 'pool') {
                window.location.href = `viewPool.html`
            }
            else if (localStorage.getItem('dataMenuToken') === 'token') {
                window.location.href = `tokenLayout.html`
            }
        });
        tableBody.appendChild(row);
    });
}
function renderTableBasedOnMenuToken() {
    const tableWrapper = document.getElementById('tableWrapper');
    let isDown = false;
    let startX;
    let scrollLeft;

    tableWrapper.addEventListener('mousedown', (e) => {
        isDown = true;
        tableWrapper.classList.add('grabbing');
        startX = e.pageX - tableWrapper.offsetLeft;
        scrollLeft = tableWrapper.scrollLeft;
    });

    tableWrapper.addEventListener('mouseleave', () => {
        isDown = false;
        tableWrapper.classList.remove('grabbing');
    });

    tableWrapper.addEventListener('mouseup', () => {
        isDown = false;
        tableWrapper.classList.remove('grabbing');
    });

    tableWrapper.addEventListener('mousemove', (e) => {
        if (!isDown) return;
        e.preventDefault();
        const x = e.pageX - tableWrapper.offsetLeft;
        const walk = (x - startX) * 1; // Điều chỉnh tốc độ kéo nếu cần
        tableWrapper.scrollLeft = scrollLeft - walk;
    });
    let dataMenuToken = localStorage.getItem('dataMenuToken');
    if (dataMenuToken && typeRenderTable[dataMenuToken]) {
        // Xóa nội dung bảng hiện tại
        const tableHead = document.querySelector('#dataTable thead tr');
        const tableBody = document.querySelector('#dataTable tbody');
        tableHead.innerHTML = '';
        tableBody.innerHTML = '';

        // Render lại bảng với dữ liệu mới
        renderTable(typeRenderTable[dataMenuToken].colum, typeRenderTable[dataMenuToken].data);
    }
}

// Gọi hàm khi trang được tải
document.addEventListener('DOMContentLoaded', renderTableBasedOnMenuToken);
